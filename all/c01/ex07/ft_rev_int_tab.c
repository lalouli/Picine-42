void	ft_rev_int_tab(int	*tab, int	size)
{
	int	i ;
	int	k ;
	int	tp;

	i = 0 ;
	k = size ;
	while (i < k / 2 )
	{
		tp = tab[i];
		tab[i] = tab[size - 1];
		tab[size - 1] = tp;
		size--;
		i++;
	}
}
